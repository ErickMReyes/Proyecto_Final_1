    <div class="content">
    Carlos Sainz sigue batiendo sus propios registros y ya acumula 12 carreras consecutivas en los puntos. El español terminó el Gran Premio de Brasil en la sexta posición y fue uno de los pilotos destacados del fin de semana por la salida que hizo en la clasificación al sprint y también por su rendimiento para mantener detrás a Sergio Pérez.
    <br> Tenemos que remontarnos a finales de junio, concretamente al Gran Premio de Estiria, para ver a Sainz fuera de los puntos. El piloto madrileño sigue aumentando su racha y ya acumula 12 carreras consecutivas puntuando. Esto le convierte en el único piloto de toda la parrilla que ha puntuado en tantas citas esta temporada.
    <br>Carlos cada vez está más cómodo con el coche y eso se traduce en un buen ritmo sobre la pista. Sin embargo, hay aspectos en los que siempre ha despuntado y continúa haciéndolo, como son las salidas.
    <br>El pasado sábado comenzó la clasificación al sprint quinto, pero en apenas unos metros escaló a la segunda posición tras una buena arrancada. Con los neumáticos blandos, Sainz adelantó a Pierre Gasly, a Sergio Pérez e incluso a Max Verstappen. Aunque terminó perdiendo la posición con el neerlandés, se las ingenió para aguantar durante 24 vueltas detrás a Pérez con la goma media.
    <br>El propio Carlos reconoció que esa salida le dio un plus de confianza y, además, le permitió sumar un punto extra en la clasificación al sprint. Por el contrario, el domingo no tuvo tanta suerte en los primeros metros, pues tuvo un problema con el embrague y también se tocó con Lando Norris, lo que le hizo perder varias posiciones. Pese a todo, el madrileño tuvo más ritmo que Charles Leclerc, a pesar de terminar justo por detrás de él. 
    <br>Las actuaciones que realiza cada fin de semana le convierten en el piloto más regular en carrera, pues no baja del Top 10 desde Estiria ni del Top 8 desde Bélgica. Su rendimiento también está siendo importante para Ferrari en la intensa batalla que libra contra McLaren por la tercera posición en el Campeonato de Constructores. Actualmente, los de Maranello están 31,5 puntos por delante. 
    
      
